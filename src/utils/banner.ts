function printBanner(text: string): void {
    const lines = text.split('\n');
    const colors = [
        chalk.yellow,
        chalk.green,
        chalk.cyan,
    ];

    lines.forEach((line, index) => {
        if (line.trim()) {
            console.log(colors[index % colors.length](line));
        } else {
            console.log(line);
        }
    });
}

